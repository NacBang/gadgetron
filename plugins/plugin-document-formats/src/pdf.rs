//! `PdfExtractor` — PDF → UTF-8 text-layer extraction via the
//! `pdf-extract` crate (W3-KL-3 / D-20260418-13).
//!
//! # Contract
//!
//! - Input: `application/pdf` bytes (with or without `; charset=` suffix).
//! - Output: [`ExtractedDocument`] with concatenated per-page text joined
//!   by the U+000C form feed character. The form feed is the chunker's
//!   page-break anchor — `StructureHint::PageBreak { byte_offset,
//!   page_number }` is emitted at every page boundary so
//!   `ChunkDraft::source_page_hint` can track citation anchors per
//!   design 11 §9.3.
//!
//! # Why `extract_text_from_mem_by_pages` (not `extract_text_from_mem`)
//!
//! `pdf-extract 0.7.x` has two entry points:
//!
//! - `extract_text_from_mem(&[u8]) -> Result<String, _>` concatenates
//!   all pages into one string **without** page separators.
//! - `extract_text_from_mem_by_pages(&[u8]) -> Result<Vec<String>, _>`
//!   returns one string per page.
//!
//! The citation pipeline needs per-page offsets (design 11 §9.3 surfaces
//! `source_page_hint` in the Penny footnote). Using the by-pages API and
//! emitting `\x0C` page separators ourselves gives us:
//! 1. Deterministic byte offsets for `PageBreak` hints.
//! 2. A convention matching pandoc's own PDF-to-text output (pandoc uses
//!    `\x0C` between pages), so future extractors that swap the backend
//!    don't have to change the chunker.
//!
//! # Error policy
//!
//! Any `pdf-extract` failure → `ExtractError::Malformed` with the
//! OutputError diagnostic embedded. The pipeline surfaces this through
//! the MCP wire as error_code `"extract_malformed_input"` per the
//! wire-frozen table in `ingest::extractor`. Internal lopdf panics are
//! caught by tokio's task boundary — no catch_unwind needed here.
//!
//! # Size / timeout
//!
//! This module does NOT enforce size limits; the caller (the pipeline)
//! is responsible for pre-filtering per design 11 §4.4. Timeouts are
//! similarly the caller's concern — `tokio::time::timeout` wraps the
//! call at the gadget layer. We keep the extractor trait impl simple.

use std::sync::Arc;

use async_trait::async_trait;
use gadgetron_core::ingest::{
    ExtractError, ExtractHints, ExtractWarning, ExtractWarningKind, ExtractedDocument, Extractor,
    StructureHint,
};

/// PDF MIME type. Prefix match — the pipeline peels off `charset`
/// parameters before calling us, but we keep the single canonical
/// entry per the trait contract (`supported_content_types` returns
/// canonical types without parameters).
const SUPPORTED: &[&str] = &["application/pdf"];

/// Page separator inserted between per-page strings. Matches pandoc's
/// `\x0C` (form feed) convention. Kept as a `pub const` so the chunker
/// can search for it without duplicating the literal.
pub const PAGE_SEPARATOR: char = '\x0C';

/// Lower bound on bytes of text per "rich" page. Used by the heuristic
/// that emits `ExtractWarningKind::LowOcrConfidence` when a PDF looks
/// scanned (i.e. the text layer is effectively empty even though the
/// PDF has multiple pages).
const MIN_TEXT_BYTES_PER_PAGE: usize = 8;

/// PDF text-layer extractor. Holds no mutable state — all concurrency
/// boils down to the `pdf-extract` call, which is self-contained.
#[derive(Debug, Default, Clone)]
pub struct PdfExtractor;

impl PdfExtractor {
    pub fn new() -> Self {
        Self
    }
}

impl PdfExtractor {
    /// Shared instance factory — most callers just want one `Arc`.
    pub fn shared() -> Arc<Self> {
        Arc::new(Self)
    }
}

#[async_trait]
impl Extractor for PdfExtractor {
    fn name(&self) -> &str {
        // The version suffix is load-bearing for debugging — audit logs
        // pin the extractor version so operators can diff extraction
        // quality across pdf-extract upgrades.
        "pdf-extract-0.7"
    }

    fn supported_content_types(&self) -> &[&str] {
        SUPPORTED
    }

    async fn extract(
        &self,
        bytes: &[u8],
        _content_type: &str,
        hints: &ExtractHints,
    ) -> Result<ExtractedDocument, ExtractError> {
        // Run the blocking PDF parse on a tokio blocking thread so the
        // current reactor thread stays responsive. `pdf-extract` issues
        // synchronous I/O against in-memory buffers but the lopdf walk
        // is CPU-heavy on large files — for a 50 MB PDF (design 11 §4.4
        // default size cap) this can take 200-400 ms.
        //
        // `bytes.to_vec()` clones — owned by the spawned task. Returning
        // `Vec<String>` (one per page) keeps the lifetime story simple;
        // the alternative (borrow the caller's slice across the await
        // point) would require `tokio::task::block_in_place` which
        // pessimizes single-threaded test runtimes.
        let owned = bytes.to_vec();
        let max_pages = hints.max_pages;
        let pages_res = tokio::task::spawn_blocking(move || {
            pdf_extract::extract_text_from_mem_by_pages(&owned)
        })
        .await
        .map_err(|join_err| {
            ExtractError::Internal(format!("pdf-extract worker join failed: {join_err}"))
        })?;

        let pages = match pages_res {
            Ok(v) => v,
            Err(e) => {
                // pdf-extract returns `OutputError` for structural
                // failures (invalid xref, bad header, encryption we
                // can't defeat). The user-facing wire code is
                // `extract_malformed_input` — see
                // `ExtractError::code()` in gadgetron-core.
                return Err(ExtractError::Malformed(format!("pdf-extract: {e:?}")));
            }
        };

        // Apply hints.max_pages: if set, truncate and emit a warning.
        // `pdf-extract` already returned everything it parsed, so this
        // is a post-filter rather than an early-exit. For a future
        // optimization, the extractor could stream pages and bail out.
        let (pages, truncated) = apply_max_pages(pages, max_pages);

        // Assemble the plain_text by joining pages with form feeds.
        // `PAGE_SEPARATOR` is one `\x0C` byte, so byte offsets are
        // deterministic: offset = len(page 0) + 1 + len(page 1) + 1 …
        let mut plain_text = String::with_capacity(pages.iter().map(|p| p.len() + 1).sum());
        let mut structure: Vec<StructureHint> = Vec::with_capacity(pages.len().saturating_sub(1));
        let mut total_text_bytes: usize = 0;
        for (idx, page) in pages.iter().enumerate() {
            if idx > 0 {
                // Emit a PageBreak hint at the byte offset of the
                // separator itself. `page_number` is 1-indexed per the
                // usual convention — matches `pdf-extract`'s error
                // messages and what operators see in PDF viewers.
                structure.push(StructureHint::PageBreak {
                    byte_offset: plain_text.len(),
                    page_number: (idx + 1) as u32,
                });
                plain_text.push(PAGE_SEPARATOR);
            }
            plain_text.push_str(page);
            total_text_bytes += page.len();
        }

        // Source metadata is free-form JSON — populate the fields the
        // citation pipeline will read: source_format, page_count,
        // extractor name (also available via `name()`).
        let source_metadata = serde_json::json!({
            "extractor": "pdf-extract-0.7",
            "source_format": "pdf",
            "page_count": pages.len(),
            "truncated_by_max_pages": truncated,
        });

        // Warnings:
        //   1. `TruncatedAtLimit` when hints.max_pages clipped pages.
        //   2. `LowOcrConfidence` when the text layer is essentially
        //      empty for a multi-page document — a scanned PDF where
        //      OCR is required. We keep the check cheap: per-page
        //      average < MIN_TEXT_BYTES_PER_PAGE characters.
        let mut warnings: Vec<ExtractWarning> = Vec::new();
        if truncated {
            warnings.push(ExtractWarning {
                kind: ExtractWarningKind::TruncatedAtLimit,
                byte_offset: None,
                message: format!("truncated to {} page(s) per hints.max_pages", pages.len()),
            });
        }
        if !pages.is_empty() && total_text_bytes < MIN_TEXT_BYTES_PER_PAGE * pages.len() {
            warnings.push(ExtractWarning {
                kind: ExtractWarningKind::LowOcrConfidence,
                byte_offset: None,
                message: format!(
                    "low text density: {} bytes across {} page(s) — likely a scanned PDF \
                     without an OCR text layer",
                    total_text_bytes,
                    pages.len()
                ),
            });
        }

        Ok(ExtractedDocument {
            plain_text,
            structure,
            source_metadata,
            warnings,
        })
    }
}

/// Clip `pages` to `max` when `max` is `Some(n)` and `n < pages.len()`.
/// Returns the truncated vec plus a bool indicating whether truncation
/// actually occurred.
fn apply_max_pages(pages: Vec<String>, max: Option<u32>) -> (Vec<String>, bool) {
    match max {
        Some(n) if (n as usize) < pages.len() => {
            let n = n as usize;
            (pages.into_iter().take(n).collect(), true)
        }
        _ => (pages, false),
    }
}

