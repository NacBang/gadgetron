    #[tokio::test]
    async fn pdf_extractor_metadata_shape() {
        // Trait metadata hooks. Name / content-type MUST match the
        // W3-KL-3 design spec — the wire surface uses `name` for
        // audit logs and `supported_content_types` to dispatch.
        let ex = PdfExtractor::new();
        assert_eq!(ex.name(), "pdf-extract-0.7");
        assert_eq!(ex.supported_content_types(), &["application/pdf"]);
    }

    #[tokio::test]
    async fn pdf_extractor_extracts_text() {
        // Happy path: a 1-page PDF with "Hello World from Gadgetron"
        // must extract the ASCII literal. `pdf-extract 0.7` prepends
        // a pair of newlines (its text cursor reset) — accept any
        // whitespace prefix so the test doesn't break on a point
        // release that changes cursor emission.
        let ex = PdfExtractor::new();
        let out = ex
            .extract(
                MINIMAL_PDF_HELLO_WORLD,
                "application/pdf",
                &ExtractHints::default(),
            )
            .await
            .expect("valid pdf must extract");
        assert!(
            out.plain_text.contains("Hello World from Gadgetron"),
            "expected 'Hello World from Gadgetron' in plain_text; got {:?}",
            out.plain_text
        );
        assert_eq!(out.source_metadata["source_format"], "pdf");
        assert_eq!(out.source_metadata["page_count"], 1);
        // Single-page → no PageBreak hint (page breaks are emitted
        // *between* pages, not before page 1).
        let page_breaks = out
            .structure
            .iter()
            .filter(|h| matches!(h, StructureHint::PageBreak { .. }))
            .count();
        assert_eq!(
            page_breaks, 0,
            "single-page pdf must emit zero PageBreak hints; got structure={:?}",
            out.structure
        );
    }

    #[tokio::test]
    async fn pdf_extractor_counts_pages_and_emits_page_breaks() {
        // Two-page PDF: expect page_count=2 and exactly one PageBreak
        // hint at the byte offset where page 2 starts.
        let ex = PdfExtractor::new();
        let out = ex
            .extract(TWO_PAGE_PDF, "application/pdf", &ExtractHints::default())
            .await
            .expect("two-page pdf must extract");
        assert_eq!(out.source_metadata["page_count"], 2);
        assert!(out.plain_text.contains("Page One Text"));
        assert!(out.plain_text.contains("Page Two Content"));
        // Form feed at the page boundary.
        let ff_count = out.plain_text.matches(PAGE_SEPARATOR).count();
        assert_eq!(
            ff_count, 1,
            "two-page pdf must have exactly one form feed; got {:?}",
            out.plain_text
        );
        // One PageBreak hint with page_number = 2.
        let page_breaks: Vec<&StructureHint> = out
            .structure
            .iter()
            .filter(|h| matches!(h, StructureHint::PageBreak { .. }))
            .collect();
        assert_eq!(page_breaks.len(), 1);
        match page_breaks[0] {
            StructureHint::PageBreak {
                page_number,
                byte_offset,
            } => {
                assert_eq!(*page_number, 2);
                // The byte offset must point at the form feed itself.
                assert_eq!(
                    out.plain_text.as_bytes().get(*byte_offset),
                    Some(&(PAGE_SEPARATOR as u8)),
                    "PageBreak.byte_offset must land on the form feed"
                );
            }
            _ => panic!("expected PageBreak variant"),
        }
    }

    #[tokio::test]
    async fn pdf_extractor_malformed_bytes_returns_error() {
        // Non-PDF bytes → `ExtractError::Malformed`. Wire code must be
        // `extract_malformed_input` so callers can branch on it.
        let ex = PdfExtractor::new();
        let err = ex
            .extract(
                b"this is not a pdf",
                "application/pdf",
                &ExtractHints::default(),
            )
            .await
            .expect_err("non-pdf bytes must be rejected");
        assert_eq!(err.code(), "extract_malformed_input");
    }

    #[tokio::test]
    async fn pdf_extractor_empty_bytes_returns_error() {
        // Empty input — pdf-extract's `lopdf::Document::load_mem`
        // returns `PdfError(Header)` or similar. Must map to
        // Malformed, NOT `Internal`.
        let ex = PdfExtractor::new();
        let err = ex
            .extract(b"", "application/pdf", &ExtractHints::default())
            .await
            .expect_err("empty bytes must be rejected");
        assert_eq!(err.code(), "extract_malformed_input");
    }

    #[tokio::test]
    async fn pdf_extractor_respects_max_pages_hint() {
        // Two-page PDF + hints.max_pages=1 → only page 1's text,
        // zero PageBreak hints, plus a `TruncatedAtLimit` warning.
        let ex = PdfExtractor::new();
        let hints = ExtractHints {
            max_pages: Some(1),
            ..ExtractHints::default()
        };
        let out = ex
            .extract(TWO_PAGE_PDF, "application/pdf", &hints)
            .await
            .expect("extract with truncation");
        assert_eq!(out.source_metadata["page_count"], 1);
        assert_eq!(out.source_metadata["truncated_by_max_pages"], true);
        assert!(out.plain_text.contains("Page One Text"));
        assert!(
            !out.plain_text.contains("Page Two Content"),
            "max_pages=1 must drop page 2 content"
        );
        // Zero form feeds because we only have one page.
        assert_eq!(out.plain_text.matches(PAGE_SEPARATOR).count(), 0);
        // Warning present.
        assert!(
            out.warnings
                .iter()
                .any(|w| matches!(w.kind, ExtractWarningKind::TruncatedAtLimit)),
            "expected TruncatedAtLimit warning; got {:?}",
            out.warnings
        );
    }
