export const dictionaries: Record<Locale, Dictionary> = { en, ko };

interface I18nValue {
  locale: Locale;
  labels: Dictionary;
  setLocale: (locale: Locale) => void;
}

const I18nContext = createContext<I18nValue>({
  locale: DEFAULT_LOCALE,
  labels: dictionaries[DEFAULT_LOCALE],
  setLocale: () => {},
});

function storedLocale(): Locale | null {
  if (typeof window === "undefined") return null;
  const value = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  return value === "en" || value === "ko" ? value : null;
}

export function currentDictionary(): Dictionary {
  return dictionaries[storedLocale() ?? DEFAULT_LOCALE];
}

export function LocaleProvider({
  children,
  initialLocale = DEFAULT_LOCALE,
}: {
  children: ReactNode;
  initialLocale?: Locale;
}) {
  const [locale, updateLocale] = useState<Locale>(initialLocale);

  useEffect(() => {
    const saved = storedLocale();
    if (saved) updateLocale(saved);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  }, [locale]);

  const setLocale = useCallback((next: Locale) => updateLocale(next), []);
  const value = useMemo<I18nValue>(
    () => ({ locale, labels: dictionaries[locale], setLocale }),
    [locale, setLocale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  return useContext(I18nContext);
}
